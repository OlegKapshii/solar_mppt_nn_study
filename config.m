function cfg = config()
%CONFIG  Центральна конфігурація системи.
%
%   cfg = config()  повертає struct з усіма параметрами симуляції.
%
%   Змінюйте значення тут, а не в окремих модулях. Щоб провести
%   дослідження по одному параметру, беріть cfg і перезаписуйте поле
%   перед передачею в run_simulation().
%
%   Приклад:
%       cfg = config();
%       cfg.clouds.avg_cloudiness_pct = 60;
%       result = run_simulation('full_day', @mppt_po, cfg);

    % =====================================================================
    % ГЕОГРАФІЯ ТА ЧАС
    % =====================================================================
    cfg.geo.latitude_deg   = 49.83;   % Львів
    cfg.geo.longitude_deg  = 23.96;   % Львів (східна додатна)
    cfg.geo.tilt_deg       = 35;      % Нахил панелі від горизонту
    cfg.geo.azimuth_deg    = 180;     % Південь (від півночі, схід додатний)
    cfg.geo.albedo         = 0.2;     % Альбедо землі (трава)

    cfg.time.day_of_year   = 172;     % 21 червня (літнє сонцестояння)
    cfg.time.dt_s          = 1;       % Крок симуляції [с]
    cfg.time.t_start_h     = 0;       % Початок дня [год]
    cfg.time.t_end_h       = 24;      % Кінець дня [год]

    % =====================================================================
    % АТМОСФЕРА ТА КЛІР-СКАЙ
    % =====================================================================
    cfg.atm.turbidity      = 0.15;    % Оптична глибина (Beer-Lambert), τ
    cfg.atm.diffuse_frac   = 0.10;    % Частка розсіяного від DNI
    cfg.atm.solar_const    = 1367;    % Сонячна стала [Вт/м²]

    % =====================================================================
    % ХМАРИ (марковська модель)
    % =====================================================================
    cfg.clouds.avg_cloudiness_pct = 40;   % Середня хмарність [%], 0..100
    cfg.clouds.active_hours       = [8 19];  % Вікно хмарності [год]
    cfg.clouds.p_clear_to_cloudy  = 0.02; % Імовірність переходу ясно→хмарно за крок часу (при 1 с і 40% hmr дає реалістичні «вікна»)
    cfg.clouds.p_cloudy_to_clear  = 0.03; % Імовірність переходу хмарно→ясно
    cfg.clouds.attenuation_range  = [0.2 0.7];  % Множник освітлення в хмарному стані
    cfg.clouds.transition_steps   = 30;   % Згладжування переходів [кроків симуляції]
    cfg.clouds.seed               = 42;

    % =====================================================================
    % ОТОЧЕННЯ
    % =====================================================================
    cfg.ambient.T_mean_C   = 20;      % Середня температура доби [°C]
    cfg.ambient.T_ampl_C   = 8;       % Амплітуда добової синусоїди
    cfg.ambient.T_peak_h   = 15;      % Час максимуму (15:00)

    % =====================================================================
    % СОНЯЧНА ПАНЕЛЬ — Kyocera KC200GT (паспортні параметри)
    %   Канонічний бенчмарк у літературі MPPT (Villalva 2009).
    % =====================================================================
    cfg.panel.Voc_stc      = 32.9;    % Напруга холостого ходу [В] @ STC
    cfg.panel.Isc_stc      = 8.21;    % Струм КЗ [А] @ STC
    cfg.panel.Vmpp_stc     = 26.3;    % Напруга MPP [В] @ STC
    cfg.panel.Impp_stc     = 7.61;    % Струм MPP [А] @ STC
    cfg.panel.Ns           = 54;      % Кількість послідовних клітин
    cfg.panel.alpha_isc    = 0.00318; % Темп. коеф. Isc [A/°C]
    cfg.panel.beta_voc     = -0.123;  % Темп. коеф. Voc [V/°C]
    cfg.panel.NOCT_C       = 47;      % Nominal Operating Cell Temperature
    cfg.panel.a_diode      = 1.3;     % Фактор ідеальності діода (1..1.5)

    % STC умови
    cfg.panel.G_stc        = 1000;    % Освітленість STC [Вт/м²]
    cfg.panel.T_stc        = 25;      % Температура STC [°C]

    % =====================================================================
    % МАСИВ ПАНЕЛЕЙ
    % =====================================================================
    cfg.array.Ns_panels    = 10;      % Панелей послідовно
    cfg.array.Np_panels    = 2;       % Паралельних гілок
    %   → Pmax_array ≈ 10*2*200 Вт = 4 кВт

    % =====================================================================
    % АЛГОРИТМ P&O
    % =====================================================================
    cfg.po.V_step          = 1.0;     % Крок збурення напруги [В] на рівні масиву
    cfg.po.update_every_s  = 1;       % Частота оновлення [с] (1 крок симуляції = 1 крок P&O)
    cfg.po.V_init_frac     = 0.7;     % Початкова напруга як частка від Voc_array

    % =====================================================================
    % НЕЙРОМЕРЕЖА
    % =====================================================================
    cfg.nn.layers          = [2 12 8 1]; % Архітектура FC: [G,T] → 12 → 8 → V_mpp
    cfg.nn.activation      = 'tanh';     % tanh або relu (тільки tanh реалізовано)
    cfg.nn.learning_rate   = 0.02;
    cfg.nn.epochs          = 800;
    cfg.nn.batch_size      = 64;
    cfg.nn.dataset_size    = 3000;
    cfg.nn.measurement_noise = 0.02;     % Шум на входах (частка) для симуляції
    cfg.nn.weights_file    = fullfile(fileparts(mfilename('fullpath')), 'nn', 'nn_weights.mat');
    cfg.nn.update_every_s  = 1;          % Частота оновлення NN-трекера

    % Нормалізація входів NN
    cfg.nn.G_norm          = 1200;       % Вт/м² (трохи вище за очік. максимум)
    cfg.nn.T_norm          = 50;         % °C
    cfg.nn.T_shift         = 25;         % центр шкали температури

    % =====================================================================
    % ВИВІД
    % =====================================================================
    cfg.io.results_dir     = fullfile(fileparts(mfilename('fullpath')), 'results');
    cfg.io.save_figures    = true;
    cfg.io.save_csv        = true;
    cfg.io.fig_dpi         = 120;
end
