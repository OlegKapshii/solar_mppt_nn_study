function [G_POA, comp, geo] = calc_solar_irradiance(t_hours, lat, lon, dayOfYear, tilt, panel_az, albedo)
% CALC_SOLAR_IRRADIANCE  Фізично-коректна модель сонячного опромінення (ясне небо).
%
% Синтаксис:
%   [G_POA, comp, geo] = calc_solar_irradiance(t_hours, lat, lon, dayOfYear, tilt, panel_az, albedo)
%
% Вхідні параметри:
%   t_hours   - вектор часу [1×N], години від 0 до 24, UTC
%   lat       - географічна широта [°], північ = позитивна
%   lon       - географічна довгота [°], схід  = позитивна
%   dayOfYear - день року (1..365)
%   tilt      - кут нахилу панелі від горизонталі [°]  (для України: 30..40°)
%   panel_az  - азимут панелі [°]: 0=Північ, 90=Схід, 180=Південь, 270=Захід
%   albedo    - альбедо поверхні [0..1]  (газон: 0.20, бетон: 0.30, сніг: 0.60)
%
% Вихідні параметри:
%   G_POA  - повне опромінення на площину панелі [Вт/м²]  (головний результат)
%   comp   - структура з декомпозицією опромінення (для аналізу):
%              .DNI       - пряме нормальне опромінення [Вт/м²]
%              .DHI       - розсіяне горизонтальне опромінення [Вт/м²]
%              .GHI       - глобальне горизонтальне опромінення [Вт/м²]
%              .beam_POA  - пряма компонента в площині панелі [Вт/м²]
%              .diff_POA  - розсіяна (небесна) компонента в площині панелі [Вт/м²]
%              .refl_POA  - відбита від землі компонента [Вт/м²]
%   geo    - структура з геометрією (для налагодження і графіків):
%              .elev_deg  - висота Сонця над горизонтом [°]
%              .zen_deg   - зенітний кут Сонця [°]
%              .cosInc    - косинус кута падіння на панель [-]
%              .AM        - оптична маса повітря (air mass) [-]
%              .sun_up    - логічний вектор: true = день, false = ніч
%              .H_deg     - годинний кут Сонця [°]
%
% Застосована фізична модель:
%   1. Геометрія: повна 5-членна формула Duffie & Beckman (враховує азимут панелі)
%   2. Air mass: формула Кастена-Янга (1989), точна до θz ≈ 89°
%   3. DNI (clear-sky): модель ASHRAE 2009 Handbook of Fundamentals
%   4. DHI (clear-sky): модель ASHRAE 2009 Handbook of Fundamentals
%   5. Компоненти POA: ізотропна модель неба Liu & Jordan (1963)
%
% Параметри атмосфери за умовчанням (tau_b=0.320, tau_d=2.200):
%   Відповідають помірним широтам (45-55°N), літньому сезону, чисте небо
%   з помірним міським аерозолем. Пікове G_POA ≈ 1000-1100 Вт/м² (Львів, червень).
%
% Ключові відмінності від спрощених підходів у solar_sim2-4:
%   - Формула косинуса падіння правильно враховує будь-який азимут панелі.
%   - DNI та DHI розраховуються незалежно за стандартною атмосферною моделлю,
%     а не з константи 1100*mu або довільних коефіцієнтів.
%   - Три компоненти POA (пряма, розсіяна, відбита) дають правильну суму.
%   - Вихід comp дозволяє виявити, яка частка втрат йде на кожну складову.
%
% Приклад використання (Львів, 22 червня, нахил 35°, азимут Південь):
%   t = 4 : (5/60) : 21;
%   [G, comp, geo] = calc_solar_irradiance(t, 49.83, 23.96, 172, 35, 180, 0.20);
%
%   figure;
%   plot(t, comp.beam_POA, 'r', t, comp.diff_POA, 'b', t, comp.refl_POA, 'g', t, G, 'k--');
%   legend('Beam (DNI на панель)', 'Diffuse (небо)', 'Reflected (земля)', 'G_{POA} (сума)');
%   xlabel('Час (год)'); ylabel('Вт/м^2'); grid on;
%
% Перевірка (чисельно):
%   Ясний день, Львів, 22 червня, θz=26.4° (полудень) → G_POA ≈ 1090 Вт/м²

% =========================================================================
%  ПАРАМЕТРИ АТМОСФЕРНОЇ МОДЕЛІ  (ASHRAE 2009, розділ 14)
% =========================================================================
% tau_b — оптична глибина прямого (beam) потоку.
%   Чисте морське повітря: ~0.25. Помірне міське: ~0.32-0.40.
tau_b = 0.320;

% tau_d — оптична глибина розсіяного (diffuse) потоку.
%   Типовий діапазон для помірних широт: 2.00-2.60.
tau_d = 2.200;

% Показники ступеня ab, ad (залежать від tau_b і tau_d за ASHRAE 2009):
% E_beam = I0n * exp(-tau_b * AM^ab)
% E_diff = I0n * cos(zen) * exp(-tau_d * AM^ad)
ab = 1.219 - 0.043*tau_b - 0.151*tau_d - 0.204*tau_b*tau_d;
ad = 0.202 + 0.852*tau_b - 0.007*tau_d - 0.357*tau_b*tau_d;

% =========================================================================
%  КОНСТАНТИ
% =========================================================================
I_sc = 1367;          % Сонячна стала [Вт/м²]
d2r  = pi / 180;      % Коефіцієнт переведення: градуси → радіани

% =========================================================================
%  КРОК 1. ПОПРАВКА НА ЕКСЦЕНТРИСИТЕТ ОРБІТИ ЗЕМЛІ
% =========================================================================
% Земля рухається по еліпсу, тому відстань до Сонця змінюється ±3.3%.
% E0 = (середня відстань / поточна відстань)^2 — безрозмірний коефіцієнт.
E0  = 1 + 0.033 * cos(2*pi * dayOfYear / 365);
I0n = I_sc * E0;     % Позаатмосферне опромінення по нормалі до Сонця [Вт/м²]

% =========================================================================
%  КРОК 2. РІВНЯННЯ ЧАСУ (Equation of Time) ТА СОНЯЧНИЙ ЧАС
% =========================================================================
% EoT враховує нерівномірність руху Землі та нахил осі.
% Результат у хвилинах — від -16 хв (жовтень) до +14 хв (лютий).
B_ang = 2*pi * (dayOfYear - 81) / 364;
EoT   = 9.87*sin(2*B_ang) - 7.53*cos(B_ang) - 1.5*sin(B_ang);  % [хвилини]

% Справжній сонячний час (True Solar Time):
%   TST [год] = UTC [год] + lon/15 [год/°] + EoT/60 [год]
% lon/15 перетворює різницю між місцевим меридіаном і нульовим.
solar_time = t_hours + lon/15 + EoT/60;   % [год]

% Годинний кут H: 0° о сонячному полудні; від'ємний вранці, позитивний ввечері.
% 15°/год = кутова швидкість обертання Землі.
H = d2r * 15 * (solar_time - 12);         % [рад]

% =========================================================================
%  КРОК 3. КУТ СХИЛЕННЯ СОНЦЯ (Solar Declination)
% =========================================================================
% Схилення δ — кут між напрямком на Сонце та площиною екватора.
% Змінюється протягом року від −23.45° (грудень) до +23.45° (червень).
delta = d2r * 23.45 * sin(d2r * 360 * (284 + dayOfYear) / 365);

% Попередньо обчислюємо тригонометрію з констант (один раз, не в циклі):
lat_r  = lat * d2r;
tilt_r = tilt * d2r;

% Конвертуємо азимут із "від Півночі" у "від Півдня" (конвенція Duffie & Beckman):
%   Південь = 0°, Захід = +90°, Північ = ±180°, Схід = -90°
gamma_p = d2r * (panel_az - 180);

sin_d   = sin(delta);    cos_d   = cos(delta);
sin_phi = sin(lat_r);    cos_phi = cos(lat_r);
sin_bet = sin(tilt_r);   cos_bet = cos(tilt_r);
sin_gam = sin(gamma_p);  cos_gam = cos(gamma_p);

% =========================================================================
%  КРОК 4. ЗЕНІТНИЙ КУТ СОНЦЯ
% =========================================================================
% cos(θz) = sin(δ)·sin(φ) + cos(δ)·cos(φ)·cos(H)
cos_zen = sin_d*sin_phi + cos_d.*cos_phi.*cos(H);
cos_zen = min(1, max(-1, cos_zen));   % Числова безпека (уникнення acos(>1))

zen_rad  = acos(cos_zen);
zen_deg  = zen_rad / d2r;
elev_deg = 90 - zen_deg;

% Сонце над горизонтом:
sun_up = (cos_zen > 0);

% =========================================================================
%  КРОК 5. КУТ ПАДІННЯ НА ПОХИЛУ ПЛОЩИНУ ПАНЕЛІ  (Incidence Angle)
% =========================================================================
% Повна 5-членна формула Duffie & Beckman (Solar Engineering, рівн. 1.6.2).
% Ця формула точна для будь-якого азимута панелі — на відміну від скороченої
% 4-членної (яка припускає орієнтацію тільки на Південь).
%
% cos(θi) = sin(δ)·sin(φ)·cos(β)
%          - sin(δ)·cos(φ)·sin(β)·cos(γ)
%          + cos(δ)·cos(φ)·cos(β)·cos(H)
%          + cos(δ)·sin(φ)·sin(β)·cos(γ)·cos(H)
%          + cos(δ)·sin(β)·sin(γ)·sin(H)
%
% де β — нахил панелі, γ — азимут від Півдня, φ — широта, δ — схилення, H — годинний кут
cosInc = sin_d * sin_phi * cos_bet ...
       - sin_d * cos_phi * sin_bet * cos_gam ...
       + cos_d .* cos_phi * cos_bet .* cos(H) ...
       + cos_d .* sin_phi * sin_bet * cos_gam .* cos(H) ...
       + cos_d .* sin_bet * sin_gam .* sin(H);

cosInc(cosInc < 0) = 0;   % Сонце за панеллю — промені не потрапляють
cosInc(~sun_up)    = 0;   % Нічний час

% =========================================================================
%  КРОК 6. ОПТИЧНА МАСА ПОВІТРЯ (Air Mass)
% =========================================================================
% AM — відносна товщина атмосфери, яку пробиває сонячний промінь.
% AM = 1 при θz=0° (сонце в зеніті), AM ≈ 40 при θz=90° (горизонт).
%
% Формула Кастена-Янга (1989) — висока точність до θz ≈ 89°:
%   AM = 1 / [cos(θz) + 0.50572·(96.07995 - θz_deg)^(-1.6364)]
AM          = zeros(size(cos_zen));
AM(sun_up)  = 1 ./ (cos_zen(sun_up) + ...
              0.50572 .* (96.07995 - zen_deg(sun_up)).^(-1.6364));
AM(~sun_up) = Inf;    % Сонця немає — AM → ∞

% =========================================================================
%  КРОК 7. ПРЯМЕ НОРМАЛЬНЕ ОПРОМІНЕННЯ — DNI  (Clear-Sky, ASHRAE)
% =========================================================================
% DNI — потік у напрямку від Сонця, виміряний на поверхні, нормальній до Сонця.
% E_b = I0n · exp(−tau_b · AM^ab)
DNI           = zeros(size(t_hours));
DNI(sun_up)   = I0n * exp(-tau_b .* AM(sun_up).^ab);

% =========================================================================
%  КРОК 8. РОЗСІЯНЕ ГОРИЗОНТАЛЬНЕ ОПРОМІНЕННЯ — DHI  (Clear-Sky, ASHRAE)
% =========================================================================
% DHI — розсіяний потік із всього небесного куполу на горизонтальну поверхню.
% E_d = I0n · cos(θz) · exp(−tau_d · AM^ad)
DHI           = zeros(size(t_hours));
DHI(sun_up)   = I0n * cos_zen(sun_up) .* exp(-tau_d .* AM(sun_up).^ad);

% =========================================================================
%  КРОК 9. ГЛОБАЛЬНЕ ГОРИЗОНТАЛЬНЕ ОПРОМІНЕННЯ — GHI
% =========================================================================
% GHI = DNI·cos(θz) + DHI  (проекція прямого + розсіяне)
GHI           = DNI .* cos_zen + DHI;
GHI(~sun_up)  = 0;

% =========================================================================
%  КРОК 10. ДЕКОМПОЗИЦІЯ НА КОМПОНЕНТИ В ПЛОЩИНІ ПАНЕЛІ (POA)
% =========================================================================

% --- 10а. Пряма компонента (Beam) ---
% Пряме випромінювання, яке потрапляє на нахилену поверхню:
beam_POA = DNI .* cosInc;

% --- 10б. Розсіяна небесна компонента (Diffuse sky) ---
% Ізотропна модель Лю-Джордана (1963): небо вважається рівномірно яскравим.
% Частка розсіяного потоку, яку "бачить" нахилена панель:
%   F_sky = (1 + cos(β)) / 2   — view factor до небесного куполу
F_sky    = (1 + cos(tilt_r)) / 2;
diff_POA = DHI * F_sky;

% --- 10в. Відбита від землі компонента (Ground-reflected) ---
%   F_ground = (1 - cos(β)) / 2  — view factor до горизонталі
F_ground = (1 - cos(tilt_r)) / 2;
refl_POA = GHI * albedo * F_ground;

% =========================================================================
%  КРОК 11. ПІДСУМКОВЕ ОПРОМІНЕННЯ В ПЛОЩИНІ ПАНЕЛІ — G_POA
% =========================================================================
G_POA          = beam_POA + diff_POA + refl_POA;
G_POA(~sun_up) = 0;

% =========================================================================
%  ФОРМУЄМО ВИХІДНІ СТРУКТУРИ
% =========================================================================
comp.DNI      = DNI;
comp.DHI      = DHI;
comp.GHI      = GHI;
comp.beam_POA = beam_POA;
comp.diff_POA = diff_POA;
comp.refl_POA = refl_POA;

geo.elev_deg  = elev_deg;
geo.zen_deg   = zen_deg;
geo.cosInc    = cosInc;
geo.AM        = AM;
geo.sun_up    = sun_up;
geo.H_deg     = H / d2r;   % Годинний кут у градусах (для налагодження)

end
