function [G_cloudy, cloud_info] = apply_cloud_model(t_hours, G_clear, cloud_pct, seed)
% APPLY_CLOUD_MODEL  Реалістична модель хмарності з часовою кореляцією.
%
% Ключова відмінність від solar_sim4:
%   У solar_sim4 хмари генеруються незалежно у кожній точці часу — це "білий
%   шум", де кожні 5 хвилин може бути абсолютно ясно, а через 5 хв — повне
%   затемнення. Реальні хмари тривають від хвилин до годин і мають плавні
%   фронти входу/виходу (рампи). Ця функція моделює саме таку поведінку.
%
% Синтаксис:
%   [G_cloudy, cloud_info] = apply_cloud_model(t_hours, G_clear, cloud_pct)
%   [G_cloudy, cloud_info] = apply_cloud_model(t_hours, G_clear, cloud_pct, seed)
%
% Вхідні параметри:
%   t_hours   - вектор часу [1×N], год (UTC)
%   G_clear   - опромінення ясного неба [1×N], Вт/м²  (з calc_solar_irradiance)
%   cloud_pct - хмарність [0..100], %  (середня частка хмарного часу за день)
%   seed      - (опційно) зерно генератора; за умовчанням = 42
%
% Вихідні параметри:
%   G_cloudy   - опромінення з хмарами [1×N], Вт/м²
%   cloud_info - структура для аналізу та графіків:
%     .cloud_factor  - коефіцієнт пропускання [0..1] для кожного моменту часу
%     .cloud_state   - поточний стан хмарності (1=ясно, 2=часткова, 3=суцільна)
%     .t_coarse      - часова вісь грубої сітки (крок 15 хв) [год]
%     .tau_coarse    - τ на грубій сітці (до інтерполяції)
%     .T_matrix      - матриця переходів Маркова (3×3), для аналізу
%
% Метод (3 рівні):
%   1. Ланцюг Маркова на грубій сітці (крок 15 хв) — забезпечує тривалість
%      хмарних подій і тимчасову кореляцію між сусідніми станами.
%
%   2. Три стани хмарності з фізично реалістичними діапазонами τ:
%        Стан 1 — ЯСНО      : τ = 0.95..1.00  (мінімальний аерозоль)
%        Стан 2 — ХМАРНО    : τ = 0.45..0.80  (розсіяні/шаруваті хмари)
%        Стан 3 — СУЦІЛЬНО  : τ = 0.08..0.40  (купчасто-дощові, щільний шар)
%
%   3. PCHIP-інтерполяція на дрібну сітку — дає плавні рампи замість
%      різких стрибків. Саме плавні зміни тестують реальну динаміку MPPT.
%
% Залежність матриці переходів від cloud_pct:
%   - Низька cloud_pct (~20%): ясний стан дуже стійкий, хмари рідкісні й короткі.
%   - Висока cloud_pct (~70%): хмарні стани стійкі, часті глибокі провали.
%   Така параметризація відповідає статистиці актинометричних спостережень
%   у помірних широтах (Метеорологічний довідник України, ДСТУ ISO 9060).
%
% Типовий виклик (заміна блоку хмар у solar_sim4_u):
%   G_cloudy = apply_cloud_model(t_hours, G_ideal, cloud_pct, 42);
%   % або з повною структурою для аналізу:
%   [G_cloudy, ci] = apply_cloud_model(t_hours, G_ideal, cloud_pct, 42);
%   plot(ci.t_coarse, ci.tau_coarse);  % Підивитись на грубу сітку Маркова

    % --- Вхідна валідація ---
    if nargin < 4,  seed = 42;  end
    cloud_pct = min(100, max(0, cloud_pct));

    rng(seed, 'twister');   % Сучасний спосіб (замість застарілого rand('seed',s))

    alpha = cloud_pct / 100;   % Нормована хмарність [0..1]

    % =========================================================================
    %  КРОК 1. ФІЗИЧНІ МЕЖІ КОЕФІЦІЄНТА ПРОПУСКАННЯ ДЛЯ КОЖНОГО СТАНУ
    % =========================================================================
    % Рядки = стани [1=ясно, 2=хмарно, 3=суцільно], стовпці = [min, max] τ.
    %
    % Значення засновані на реальних вимірюваннях:
    %   τ~1.00 : ясне небо, ледь помітний аерозоль
    %   τ~0.60 : шарувато-купчасті хмари (Sc), розсіяне хмарне поле
    %   τ~0.20 : суцільний шар Ns або потужний Cb над усім небом
    tau_range = [0.95, 1.00;   % Стан 1: ясно
                 0.45, 0.80;   % Стан 2: хмарно (часткове покриття)
                 0.08, 0.40];  % Стан 3: суцільна хмарність

    % =========================================================================
    %  КРОК 2. МАТРИЦЯ ПЕРЕХОДІВ ЛАНЦЮГА МАРКОВА  T(i→j)
    % =========================================================================
    % T(i,j) = ймовірність переходу зі стану i у стан j за один крок (15 хв).
    %
    % Логіка параметризації через alpha:
    %
    %  Persistence (діагональ): наскільки стан "прилипає" до себе.
    %   - Ясний стан: при низькій хмарності дуже стійкий (~2 год),
    %     при високій хмарності — нестійкий (~30 хв).
    %   - Частковий: помірна стійкість, незалежно від хмарності.
    %   - Суцільний: при низькій хмарності швидко розсіюється,
    %     при високій — дуже стійкий (~3 год).
    %
    %  Одиниця часу = 15 хв.
    %  Середній час перебування у стані = dt / (1 - P_stay) [хв]
    %  Приклад: P_stay=0.93 → середній час ~3.6 год; P_stay=0.60 → ~37 хв

    p_stay_clear    = 0.93 - 0.45 * alpha;   % α=0: 0.93 (~3.5 год) → α=1: 0.48 (~29 хв)
    p_stay_partial  = 0.60 + 0.20 * alpha;   % α=0: 0.60 (~37 хв)  → α=1: 0.80 (~75 хв)
    p_stay_overcast = 0.55 + 0.35 * alpha;   % α=0: 0.55 (~33 хв)  → α=1: 0.90 (~150 хв)

    % Off-diagonal: куди переходить стан при "виході"?
    % При виході з будь-якого стану, перевага хмарних станів зростає з alpha.

    % Зі стану Clear (1):
    p_c_to_p = (1 - p_stay_clear) * (1 - 0.4*alpha);  % → Partial
    p_c_to_o = (1 - p_stay_clear) * 0.4 * alpha;       % → Overcast (рідко при малій alpha)

    % Зі стану Partial (2):
    p_p_to_c = (1 - p_stay_partial) * (1 - alpha);     % → Clear (менше при великій alpha)
    p_p_to_o = (1 - p_stay_partial) * alpha;            % → Overcast (більше при великій alpha)

    % Зі стану Overcast (3):
    p_o_to_c = (1 - p_stay_overcast) * (1 - alpha) * 0.4;          % → Clear (рідко)
    p_o_to_p = (1 - p_stay_overcast) * (1 - 0.4*(1 - alpha));      % → Partial (частіше)

    % Складаємо матрицю 3×3
    T = [p_stay_clear,    p_c_to_p,        p_c_to_o;
         p_p_to_c,        p_stay_partial,   p_p_to_o;
         p_o_to_c,        p_o_to_p,         p_stay_overcast];

    % Нормалізація рядків (числова безпека — рядок завжди = 1.0)
    for i = 1:3
        row_sum = sum(T(i,:));
        if row_sum > 0
            T(i,:) = T(i,:) / row_sum;
        end
    end

    % =========================================================================
    %  КРОК 3. ВИЗНАЧЕННЯ АКТИВНИХ ДЕННИХ ГОДИН
    % =========================================================================
    % Хмарний стан змінюється тільки коли є сонце. Нічний час = завжди ясно.
    % Беремо невеликий буфер до/після виходу G_clear > 0.
    t_day_start = 5.5;   % Трохи до сходу (~05:30)
    t_day_end   = 21.0;  % Після заходу (~21:00)

    % =========================================================================
    %  КРОК 4. СИМУЛЯЦІЯ ЛАНЦЮГА МАРКОВА (крок 15 хв)
    % =========================================================================
    dt_coarse = 15 / 60;                          % 15 хв → в годинах
    t_coarse  = t_day_start : dt_coarse : t_day_end;
    N_coarse  = length(t_coarse);

    states     = zeros(1, N_coarse, 'uint8');
    tau_coarse = zeros(1, N_coarse);

    % Початковий стан: за рівноважним розподілом, що відповідає alpha
    if alpha < 0.33
        s0 = 1;    % Переважно ясно
    elseif alpha < 0.66
        s0 = 2;    % Часткова хмарність
    else
        s0 = 3;    % Переважно суцільно
    end
    states(1) = s0;
    tau_coarse(1) = tau_range(s0,1) + (tau_range(s0,2) - tau_range(s0,1)) * rand();

    % Основний цикл Маркова
    for k = 2 : N_coarse
        s_prev = states(k-1);

        % Метод зворотної CDF: вибираємо наступний стан за таблицею T
        r    = rand();
        cdf  = cumsum(T(s_prev, :));
        s_next = find(r <= cdf, 1, 'first');
        if isempty(s_next)
            s_next = s_prev;   % Числова безпека
        end
        states(k) = s_next;

        if s_next ~= s_prev
            % Зміна стану → вибираємо нове τ рівномірно з нового діапазону
            lo = tau_range(s_next, 1);
            hi = tau_range(s_next, 2);
            tau_coarse(k) = lo + (hi - lo) * rand();
        else
            % Той самий стан → малий випадковий дрейф (хмара не ідеально стала)
            % Дрейф ±5% від ширини діапазону, обрізається по межах стану
            lo    = tau_range(s_next, 1);
            hi    = tau_range(s_next, 2);
            drift = (rand() - 0.5) * 0.10 * (hi - lo);
            tau_coarse(k) = min(hi, max(lo, tau_coarse(k-1) + drift));
        end
    end

    % =========================================================================
    %  КРОК 5. ІНТЕРПОЛЯЦІЯ НА ДРІБНУ СІТКУ  (PCHIP — плавні рампи)
    % =========================================================================
    % Навіщо PCHIP, а не лінійна чи spline?
    %
    %  Лінійна: дає зламані кути — різкі зміни нахилу в вузлах. Помітно на графіку.
    %  Cubic spline: зберігає гладкість, але може давати осциляції між вузлами
    %                (ефект Рунге) — τ може виходити за межі [0,1].
    %  PCHIP: монотонна між вузлами, без осциляцій, гладка — найближче до
    %         реального "плавного фронту" хмари.
    tau_fine = pchip(t_coarse, tau_coarse, t_hours);

    % Обрізаємо до фізичних меж (PCHIP рідко, але може злегка вийти за межі)
    tau_fine = min(1.00, max(0.05, tau_fine));

    % Вночі та поза активними годинами: хмарний фактор = 1 (ясно)
    tau_fine(G_clear < 1.0) = 1.0;

    % Поза денним діапазоном Маркова — теж ясно (екстраполяція не потрібна)
    tau_fine(t_hours < t_day_start) = 1.0;
    tau_fine(t_hours > t_day_end)   = 1.0;

    % =========================================================================
    %  КРОК 6. ВИХІДНІ ДАНІ
    % =========================================================================
    G_cloudy = G_clear .* tau_fine;

    cloud_info.cloud_factor = tau_fine;
    cloud_info.cloud_state  = round(interp1(t_coarse, double(states), ...
                                   t_hours, 'nearest', 'extrap'));
    cloud_info.t_coarse  = t_coarse;
    cloud_info.tau_coarse = tau_coarse;
    cloud_info.T_matrix  = T;

end
