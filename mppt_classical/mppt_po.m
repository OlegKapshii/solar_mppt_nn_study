% Класичний P&O (Perturb & Observe) алгоритм MPPT
% Відслідковування точки максимальної потужності шляхом корегування напруги

function [V_new, P_current] = mppt_po(V_prev, P_prev, V_current, P_current, dV_step)
    % Входи:
    %   V_prev - напруга на попередньому кроці [V]
    %   P_prev - потужність на попередньому кроці [W]
    %   V_current - поточна напруга [V]
    %   P_current - поточна потужність [W]
    %   dV_step - крок зміни напруги [V]
    %
    % Виходи:
    %   V_new - нова встановлена напруга [V]
    %   P_current - поточна потужність (повертаємо як є)
    
    % За замовчуванням крок = 0.5 V (на масиві 10×2 з Voc≈329 В)
    if nargin < 5
        dV_step = 0.5;
    end

    % Обмеження напруги для масиву 10s × 2p KC200GT
    % Voc_arr ≈ 329 V, Vmp_arr ≈ 263 V (паспорт), Vmp фактичний ~275 V (модель)
    V_min = 30;    % ~ 0.1 * Voc_arr — нижче цього панель не дає корисної потужності
    V_max = 320;   % ~ 0.97 * Voc_arr — вище майже немає струму
    
    % Розраховуємо різниці
    dP = P_current - P_prev;
    dV = V_current - V_prev;
    
    % P&O алгоритм
    if abs(dP) < 0.1  % Потужність практично не змінилась
        % На оптимумі або близько до нього - не змінюємо напругу
        V_new = V_current;
    else
        % Потужність змінилась - змінюємо напругу
        if dP > 0
            % Потужність збільшилась - рухаємся в напрямку ΔV
            if dV > 0
                % dV був позитивний, Р збільшилась - продовжуємо в тому ж напрямку
                V_new = V_current + dV_step;
            else
                % dV був негативний, Р збільшилась - розворот
                V_new = V_current - dV_step;
            end
        else
            % Потужність зменшилась - розворот
            if dV > 0
                % dV був позитивний, Р зменшилась - розворот
                V_new = V_current - dV_step;
            else
                % dV був негативний, Р зменшилась - розворот
                V_new = V_current + dV_step;
            end
        end
    end
    
    % Застосуємо обмеження
    V_new = max(V_min, min(V_max, V_new));
    
end
