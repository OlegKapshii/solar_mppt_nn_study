% Адаптивна версія P&O MPPT з використанням даних про освітленість (піранометра)
%
% На відміну від стандартної P&O, цей алгоритм:
%   1. Використовує G (освітленість) для адаптивного вибору dV_step
%   2. Змінює стратегію поведінки залежно від швидкості змін освітленості
%   3. Зменшує крок біля MPP для більш точної підтримки оптимуму
%   4. Прискорює пошук при великих змінах освітленості
%
% Замість чистого feedback-керування, використовує також feedforward інформацію

function V_next = mppt_po_adaptive(V_prev, P_prev, V_curr, P_curr, G_curr, G_prev, dV_step_base)
    % Входи:
    %   V_prev, P_prev - напруга та потужність на попередньому кроці
    %   V_curr, P_curr - напруга та потужність на поточному кроці
    %   G_curr - поточна освітленість [W/m²]
    %   G_prev - попередня освітленість [W/m²]
    %   dV_step_base - базовий крок напруги [V]
    %
    % Вихід:
    %   V_next - рекомендована напруга для наступного кроку

    % Класичне P&O ядро
    dP = P_curr - P_prev;
    dV = V_curr - V_prev;
    
    % Стандартне P&O рішення
    if dP > 0
        if dV > 0
            direction = 1;  % Продовжуємо в тому ж напрямку
        else
            direction = -1;
        end
    else
        if dV > 0
            direction = -1;  % Розворот
        else
            direction = 1;
        end
    end
    
    % ══════════════════════════════════════════════════════════════════
    % АДАПТАЦІЯ 1: Крок напруги залежить від освітленості
    % ══════════════════════════════════════════════════════════════════
    % При низькій освітленості - дрібніший крок для точності
    % При високій освітленості - більший крок для швидкості
    
    G_normalized = G_curr / 1000;  % Нормалізація (STC = 1000 W/m²)
    
    % Крок варіює від 0.2V (при 100 W/m²) до 1.0V (при 1000 W/m²)
    min_step = 0.2;
    max_step = 1.0;
    step_adaptive = min_step + (max_step - min_step) * max(0, min(1, G_normalized));
    
    % ══════════════════════════════════════════════════════════════════
    % АДАПТАЦІЯ 2: Залежність від швидкості змін освітленості
    % ══════════════════════════════════════════════════════════════════
    % Якщо освітленість швидко змінюється, NN-подібна стратегія:
    % Робимо більший крок для швидкого переходу до нової оптимальної напруги
    
    dG = G_curr - G_prev;
    dG_relative = dG / max(1, G_prev);  % Відносна зміна освітленості
    
    if abs(dG_relative) > 0.1  % Велика зміна освітленості (>10%)
        % Раптова зміна - роблю більший крок для швидкого пошуку
        step_multiplier = 1.5;
    elseif abs(dG_relative) < 0.01  % Мала зміна (<1%)
        % Стабільна умова - дрібніший крок для точності
        step_multiplier = 0.7;
    else
        % Нормальний режим - базовий крок
        step_multiplier = 1.0;
    end
    
    dV_step = step_adaptive * step_multiplier * dV_step_base / 0.8;  % Нормалізація до базового
    
    % ══════════════════════════════════════════════════════════════════
    % АДАПТАЦІЯ 3: Демпінг біля очікуваної MPP
    % ══════════════════════════════════════════════════════════════════
    % На основі G можемо оцінити, де повинна бути V_opt за аналітичною моделлю
    % (наближена залежність V_opt від G)
    
    % Для KC200GT: V_opt зміняється слабко з G (більше залежить від T)
    % Але при дуже низькій G V_opt може зміщуватися
    
    % При G < 200 W/m² система близька до краю - більш обережна поведінка
    if G_curr < 200
        step_multiplier = step_multiplier * 0.6;  % Ще дрібніший крок
    end
    
    % ══════════════════════════════════════════════════════════════════
    % АДАПТАЦІЯ 4: Гістерезис близько MPP
    % ══════════════════════════════════════════════════════════════════
    % Якщо мікро-осциляції (малі dP), не рухаємо на великий крок
    
    if abs(dP) < 50  % Малі зміни потужності (< 50W)
        step_multiplier = step_multiplier * 0.5;
    end
    
    final_step = dV_step * step_multiplier;
    
    % Команда на наступний крок
    V_next = V_curr + direction * final_step;
    
    % Обмеження (10-80V для масиву)
    V_next = max(10, min(80, V_next));
end
