function [V_cmd, state] = mppt_ideal(G, T_cell, V_prev, P_prev, state, cfg)
%MPPT_IDEAL  «Оракул» — завжди видає справжню V_mpp від pv_mpp.
%
%   Використовується як верхня межа ефективності — теоретичний максимум.
%   Сам по собі не є реалістичним алгоритмом, бо передбачає миттєвий
%   доступ до істинної V_mpp (яку MPPT-алгоритми мають знаходити).
%
%   Вхід/вихід аналогічний до інших трекерів для уніфікації інтерфейсу:
%     G       — поточна освітленість [Вт/м²]
%     T_cell  — температура клітин [°C]
%     V_prev  — попередня напруга (не використовується)
%     P_prev  — попередня потужність (не використовується)
%     state   — стан трекера (struct), ініціалізується порожнім
%     cfg     — конфіг
%
%   Повертає V_cmd і оновлений state.

    if nargin < 6, cfg = config(); end
    if isempty(state), state = struct(); end

    [V_cmd, ~, ~] = pv_mpp(G, T_cell, cfg);

    state.name = 'ideal';
end
